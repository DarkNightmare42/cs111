#include <iostream>
using namespace std;

int main(){

  cout << "0 % 2 = " << 0 % 2 << endl; // 0 
  cout << "1 % 2 = " << 1 % 2 << endl; // 1
  cout << "2 % 2 = " << 2 % 2 << endl; //0
  cout << "0 % 3 = " << 0 % 3 << endl; //0
  cout << "1 % 3 = " << 1 % 3 << endl; //1
  cout << "123 % 10 = " << 123 % 10 << endl; //3 
  cout << "123 / 10 = " << 123 / 10 << endl; //12

  return 0;
} 
